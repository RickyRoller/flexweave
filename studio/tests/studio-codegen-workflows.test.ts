test("codegen check detects stale, missing, and unexpected managed files without writing", async () => {
  const root = copyMinimalFixture();
  const configPath = join(root, "studio.config.ts");
  const stalePath = join(root, "generated/abilities/generated.rs");
  const missingPath = join(root, "generated/tags/generated.rs");
  const unexpectedPath = join(root, "generated/effects/unused.rs");
  const originalStale = readFileSync(stalePath, "utf-8");

  writeFileSync(stalePath, `${originalStale}// stale\n`);
  rmSync(missingPath);
  writeFileSync(
    unexpectedPath,
    '//! Generated by Flexweave Studio for effects.\n\npub const OLD: &str = "old";\n',
  );

  const result = await codegenStudioProject({ check: true, configPath });

  expect(result.ok).toBe(false);
  expect(result.diagnostics.map((diagnostic) => diagnostic.code).toSorted()).toEqual([
    "generated-missing",
    "generated-stale",
    "generated-unexpected",
  ]);
  expect(readFileSync(stalePath, "utf-8")).toBe(`${originalStale}// stale\n`);
  expect(existsSync(missingPath)).toBe(false);
  expect(existsSync(unexpectedPath)).toBe(true);
});

test("extension generated targets run through the target registry", async () => {
  const checked = await codegenStudioProject({
    check: true,
    configPath: generatedTargetFixtureConfigPath,
    targets: ["synthetic-summary"],
  });
  expect(checked.ok).toBe(true);
  expect(checked.targets.map((target) => target.target)).toEqual(["tags", "synthetic-summary"]);
  expect(checked.targets.find((target) => target.target === "synthetic-summary")?.files).toEqual([
    {
      path: join(fixtureRoot, "generated/synthetic/summary.txt"),
      status: "fresh",
      target: "synthetic-summary",
    },
  ]);

  const unknown = await codegenStudioProject({
    check: true,
    configPath: generatedTargetFixtureConfigPath,
    targets: ["unknown"],
  });
  expect(unknown.ok).toBe(false);
  expect(unknown.diagnostics[0]).toMatchObject({
    code: "unknown-codegen-target",
  });
  expect(unknown.diagnostics[0]?.hint).toContain("synthetic-summary");
});

test("extension targets can replace disabled built-in target ids", async () => {
  const root = copyMinimalFixture();
  const shadowOutputDir = join(root, "generated/shadow-tags");
  mkdirSync(shadowOutputDir, { recursive: true });
  writeFileSync(join(shadowOutputDir, "shadow.txt"), "consumer tags\n");

  const generatedTarget = defineStudioGeneratedTarget({
    id: "tags",
    label: "Consumer tags",
    plan: ({ outputDir }) => ({
      files: [
        {
          path: join(outputDir, "shadow.txt"),
          value: "consumer tags\n",
        },
      ],
    }),
  });
  const validated = validateStudioConfig(
    defineStudioConfig({
      catalogRoot: "catalog",
      codegen: {
        builtInTargets: [],
        outputDirs: {
          tags: "generated/shadow-tags",
        },
      },
      extensions: [
        defineStudioExtension({
          generatedTargets: [generatedTarget],
          id: "consumer-generated-targets",
        }),
      ],
      hooks: {
        dir: "runtime-hooks",
      },
      rust: {
        flexweaveModule: "flexweave",
      },
    }),
    {
      configDir: root,
      configPath: join(root, "studio.config.ts"),
    },
  );

  expect(validated.ok).toBe(true);
  expect(validated.config?.codegen.builtInTargets).toEqual([]);

  const checked = await codegenStudioProject({
    check: true,
    config: validated.config,
  });
  expect(checked.ok).toBe(true);
  expect(checked.targets).toEqual([
    {
      files: [
        {
          path: join(shadowOutputDir, "shadow.txt"),
          status: "fresh",
          target: "tags",
        },
      ],
      label: "Consumer tags",
      target: "tags",
    },
  ]);
});

