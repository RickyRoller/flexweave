test("codegen writes only configured outputs, preserves hooks, and reports orphan hooks", async () => {
  const root = copyMinimalFixture();
  const configPath = join(root, "studio.config.ts");
  const hookPath = join(root, "runtime-hooks/minimal_execution.rs");
  const hookValue = "//! consumer-owned hook\n\npub fn minimal_execution() {}\n";
  const libPath = join(root, "runtime-hooks/lib.rs");
  const modPath = join(root, "runtime-hooks/mod.rs");
  const orphanPath = join(root, "runtime-hooks/orphan.rs");

  rmSync(join(root, "generated/abilities/generated.rs"));
  writeFileSync(hookPath, hookValue);
  writeFileSync(libPath, "//! hook module index\n");
  writeFileSync(modPath, "//! hook module index\n");
  writeFileSync(orphanPath, "//! orphan runtime hook\n");

  const result = await codegenStudioProject({ configPath });
  const loaded = await loadStudioConfig({ configPath });
  expect(loaded.ok).toBe(true);
  const outputDirs = Object.values(loaded.config?.paths.codegen.outputDirs ?? {});
  const hookDirs = [loaded.config?.paths.hooks.dir, loaded.config?.paths.hooks.testStubsDir].filter(
    (path): path is string => typeof path === "string",
  );

  expect(result.ok).toBe(true);
  expect(existsSync(join(root, "generated/abilities/generated.rs"))).toBe(true);
  expect(readFileSync(hookPath, "utf-8")).toBe(hookValue);
  expect(
    result.diagnostics.filter((diagnostic) => diagnostic.code === "orphan-runtime-hook"),
  ).toEqual([
    expect.objectContaining({
      path: "runtime-hooks/orphan.rs",
    }),
  ]);
  expect(existsSync(orphanPath)).toBe(true);
  for (const target of studioCodegenTargets) {
    expect(
      result.targets
        .find((summary) => summary.target === target)
        ?.files.every((file) => outputDirs.some((outputDir) => pathContains(outputDir, file.path))),
    ).toBe(true);
  }
  expect(
    result.hooks.every((hook) => hookDirs.some((hookDir) => pathContains(hookDir, hook.path))),
  ).toBe(true);
});

test("codegen rolls back generated files and runtime hook stubs when hook writes fail", async () => {
  const root = copyMinimalFixture();
  const configPath = join(root, "studio.config.ts");
  const generatedPath = join(root, "generated/executions/generated.rs");
  const originalGenerated = readFileSync(generatedPath, "utf-8");
  const hookPath = join(root, "runtime-hooks/rollback_runtime_hook.rs");
  const testStubsRoot = join(root, "generated-hook-tests");
  const testHookPath = join(testStubsRoot, "rollback_runtime_hook.rs");

  writeFileSync(
    join(root, "catalog/executions/rollback_execution.json"),
    `${JSON.stringify({
      hook: "rollback_runtime_hook",
      id: "rollback_execution",
      kind: "execution",
      label: "Rollback execution",
    })}\n`,
  );
  rmSync(testStubsRoot, { force: true, recursive: true });
  writeFileSync(testStubsRoot, "not a directory\n");

  const result = await codegenStudioProject({ configPath, targets: ["executions"] });

  expect(result.ok).toBe(false);
  expect(result.diagnostics).toContainEqual(
    expect.objectContaining({ code: "codegen-write-failed" }),
  );
  expect(readFileSync(generatedPath, "utf-8")).toBe(originalGenerated);
  expect(existsSync(hookPath)).toBe(false);
  expect(existsSync(testHookPath)).toBe(false);
  expect(readFileSync(testStubsRoot, "utf-8")).toBe("not a directory\n");
});
